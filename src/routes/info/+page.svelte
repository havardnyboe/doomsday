	<p>
		Dommedagsregelen, Dommedagsalgoritmen eller Dommedagsmetoden er en <a
			href="https://no.wikipedia.org/wiki/Algoritme_for_%C3%A5_bestemme_ukedag"
			>algoritme for å beregne ukedagen</a
		>
		for en gitt dato. Siden den
		<a href="https://no.wikipedia.org/wiki/Den_gregorianske_kalenderen">gregorianske kalenderen</a>
		beveger seg i sykluser på 400 år, gir den en
		<a href="https://en.wikipedia.org/wiki/Perpetual_calendar">evigvarende kalender</a>. Algoritmen
		ble laget av <a href="https://no.wikipedia.org/wiki/John_Horton_Conway">John Conway</a> i 1973, og
		er designet slik at den kan utføres med enkel hoderegning. Den utnytter at hvert år har en bestemt
		ukedag hvor visse datoer som en enkle og huske, kalt dommedager, faller; for eksempel faller siste
		dagen i februar, 4. april (4/4), 6. juni (6/6), 8. august (8/8), 10. oktober (10/10) og 12. desember
		(12/12) på samme ukedag i året.
	</p>
